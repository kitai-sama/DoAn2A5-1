private void processFileWithStreamCipher(String inputFilePath, String outputFilePath) throws Exception {
    long combinedKey = generateCombinedKey(); // Lấy khóa đã được đảm bảo khác 0

    // Sử dụng try-with-resources để đảm bảo file luôn được đóng đúng cách
    try (FileInputStream fis = new FileInputStream(inputFilePath);
         FileOutputStream fos = new FileOutputStream(outputFilePath)) {

        byte[] buffer = new byte[8192]; // Tăng kích thước buffer để cải thiện hiệu năng
        int bytesRead;
        long currentKeySegment = combinedKey; // Khởi tạo luồng khóa

        // Các hằng số nguyên tố lớn để tạo luồng khóa giả ngẫu nhiên
        final long MULTIPLIER = 0x5DEECE66DL;
        final long ADDEND = 0xBL;
        final long MASK = (1L << 48) - 1;

        while ((bytesRead = fis.read(buffer)) != -1) {
            for (int i = 0; i < bytesRead; i++) {
                // XOR từng byte với một byte từ luồng khóa
                buffer[i] = (byte) (buffer[i] ^ (currentKeySegment & 0xFF));
                
                // CẬP NHẬT LUỒNG KHÓA (LOGIC MỚI)
                // Sử dụng công thức của một bộ sinh số giả ngẫu nhiên (LCG)
                // để đảm bảo luồng khóa luôn thay đổi và không thể đoán trước một cách dễ dàng.
                currentKeySegment = (currentKeySegment * MULTIPLIER + ADDEND) & MASK;
            }
            fos.write(buffer, 0, bytesRead);
        }
    }
}

    
