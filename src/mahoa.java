        // Hàm trợ giúp để tạo khóa duy nhất từ khóa chia sẻ và số khung
private long generateCombinedKey() throws NumberFormatException {
    // 1. Lấy và làm sạch đầu vào
    String sharedKeyStr = txtKey.getText().trim();
    String frameNumberStr = txtframe.getText().trim();

    // 2. Kiểm tra trống
    if (sharedKeyStr.isEmpty() || frameNumberStr.isEmpty()) {
        throw new NumberFormatException("Khóa chia sẻ và/hoặc số khung không được để trống.");
    }
    
    // 3. Loại bỏ "0x" (nếu có)
    if (frameNumberStr.toLowerCase().startsWith("0x")) {
        frameNumberStr = frameNumberStr.substring(2);
    }

    try {
        // 4. Chuyển đổi
        long sharedKeyValue = sharedKeyStr.hashCode();
        long frameNumberValue = Long.parseLong(frameNumberStr, 16);

        // 5. Kết hợp và ĐẢM BẢO KHÔNG BẰNG 0
        long combinedKey = sharedKeyValue ^ frameNumberValue;
        
        // Nếu kết quả bằng 0, ta thay đổi nó thành một giá trị mặc định.
        if (combinedKey == 0) {
            return 0xDEADBEEFCAFEBABEL; // Một giá trị ngẫu nhiên, lớn và không bằng 0
        }
        
        return combinedKey;

    } catch (NumberFormatException e) {
        throw new NumberFormatException("Số khung '" + txtframe.getText() + "' không phải là một số hexa hợp lệ.");
    }
}

