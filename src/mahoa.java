   // Hàm đệ quy để xử lý mã hóa hoặc giải mã một thư mục và tất cả các thư mục con
private void processDirectoryRecursively(String inputDirPath, String outputDirPath, boolean isEncrypting) throws Exception {
    File inputDir = new File(inputDirPath);
    File outputDir = new File(outputDirPath);
    if (!outputDir.exists()) { outputDir.mkdirs(); }
    File[] filesList = inputDir.listFiles();
    if (filesList == null) return;

    for (File f : filesList) {
        // --- THAY ĐỔI Ở ĐÂY ---
        String outputFilePath = new File(outputDir, f.getName()).getAbsolutePath();
        if (isEncrypting) {
            if (f.isFile()) {
                // Không thêm đuôi .enc nữa
                processFileWithStreamCipher(f.getAbsolutePath(), outputFilePath); 
            } else if (f.isDirectory()) {
                processDirectoryRecursively(f.getAbsolutePath(), outputFilePath, true);
            }
        } else { // isDecrypting
            // Vì không có đuôi .enc, chúng ta sẽ giải mã mọi file
            if (f.isFile()) {
                processFileWithStreamCipher(f.getAbsolutePath(), outputFilePath);
            } else if (f.isDirectory()) {
                processDirectoryRecursively(f.getAbsolutePath(), outputFilePath, false);
            }
        }
    }
}
    
